  <script>
    // array to store the messages
    let messages = [];

    // get references to the input fields and the chat window
    let nameInput = document.getElementById("name-input-form-input");
    let messageInput = document.getElementById("message-input");
    let chatWindow = document.getElementById("chat-window");
    let inputForm = document.getElementById("input-form");
    let nameInputForm = document.getElementById("name-input-form");
    let name;

    // function to add a message to the chat window and the messages array
    function addMessage(name, message) {
      let newMessage = document.createElement("div");
      newMessage.classList.add("message");
      newMessage.innerHTML = "<span class='timestamp'>" + new Date().toLocaleString() + "</span><br>" + name + ": " + message;
      chatWindow.appendChild(newMessage);
      messages.push({name: name, message: message});
    }

    // add event listener to the send button
    document.getElementById("send-button").addEventListener("click", function() {
      // get the values of the message input
      let message = messageInput.value;
      // add the message to the chat window and the messages array
      addMessage(name, message);
// clear the input fields
messageInput.value = "";
});
    // add event listener to the submit button
document.getElementById("name-form-button").addEventListener("click", function() {
  // get the values of the name input
  name = nameInput.value;
  nameInputForm.style.display = "none";
  inputForm.style.display = "flex";
  messageInput.disabled = false;
  document.getElementById("send-button").disabled = false;
});
